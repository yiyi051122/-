def normalize_disease_name(name):
    for standard, aliases in DISEASE_ALIASES.items():
        if name == standard or name in aliases:
            return standard
    return None


def is_valid_symptom(text):
    invalid_patterns = ["不出现", "无", "正常", "健康", "抗病"]
    for pattern in invalid_patterns:
        if pattern in text:
            return False
    return 2 <= len(text) <= 50


def is_context_relevant(text, entity_type):
    positive = CONTEXT_INDICATORS["positive"]
    for indicator in positive:
        if indicator in text:
            return True
    return False
